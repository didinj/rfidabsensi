    private void saveData() {
        try {
            absEM.getTransaction().commit();
            absEM.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            absEM.getTransaction().begin();
            List<absensi.entity.AbsensiLog> merged = new ArrayList<absensi.entity.AbsensiLog>(absList.size());
            for (absensi.entity.AbsensiLog s : absList) {
                merged.add(absEM.merge(s));
            }
            absList.clear();
            absList.addAll(merged);
        }
    }
    
    private void refreshData() {
        absEM.getTransaction().rollback();
        absEM.getTransaction().begin();
        java.util.Collection data = absQuery.getResultList();
        for (Object entity : data) {
            absEM.refresh(entity);
        }
        absList.clear();
        absList.addAll(data);
    }
    
