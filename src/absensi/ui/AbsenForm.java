    private void newButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newButtonActionPerformed
        absensi.entity.AbsensiLog s = new absensi.entity.AbsensiLog();
        absEM.persist(s);
        absList.add(s);
        int row = absList.size() - 1;
        absTable.setRowSelectionInterval(row, row);
        absTable.scrollRectToVisible(absTable.getCellRect(row, 0, true));
        clearForm();
        enabledForm();
        nikText.requestFocusInWindow();
        saveButton.setEnabled(true);
    }//GEN-LAST:event_newButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int[] selected = absTable.getSelectedRows();
        List<absensi.entity.AbsensiLog> toRemove = new ArrayList<>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            absensi.entity.AbsensiLog s = absList.get(absTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(s);
            absEM.remove(s);
        }
        absList.removeAll(toRemove);
        saveData();
        refreshData();
        clearForm();
        disabledForm();
        saveButton.setEnabled(false);
        deleteButton.setEnabled(false);
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        saveData();
        refreshData();
        clearForm();
        disabledForm();
        saveButton.setEnabled(false);
        deleteButton.setEnabled(false);
    }//GEN-LAST:event_saveButtonActionPerformed

